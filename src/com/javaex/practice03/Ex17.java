import java.util.Scanner;

public class Ex17 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int i = sc.nextInt();
		for(int y=i;y>=1;y--) {
			
			for(int x = y; x<=y; x--) {
				System.out.print("*");
			}
			
		
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		sc.close();

	}

}
